#Script that calculates the simple interest earned on an investment
principal = 1000  # Principal Amount (P)
rate = 0.05       # Annual interest rate (R)
time = 3          # Time in years (I)
result = principal * rate * time
interest = result    # The formula for simple interest is (I = P * R * T)

print("For principal = 1000, rate = 0.05, and time = 3")
print("The simple interest is: ",float(interest))
